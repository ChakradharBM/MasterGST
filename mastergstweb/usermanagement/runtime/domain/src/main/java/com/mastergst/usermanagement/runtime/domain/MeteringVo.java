package com.mastergst.usermanagement.runtime.domain;

public class MeteringVo {
	private String gstnusername;
	private String ipusr;
	private String type;
	private String servicename;
	private String status;
	private String size;
	private String cost;
	private String starttime;
	
	public String getGstnusername() {
		return gstnusername;
	}
	public void setGstnusername(String gstnusername) {
		this.gstnusername = gstnusername;
	}
	public String getIpusr() {
		return ipusr;
	}
	public void setIpusr(String ipusr) {
		this.ipusr = ipusr;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getServicename() {
		return servicename;
	}
	public void setServicename(String servicename) {
		this.servicename = servicename;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public String getCost() {
		return cost;
	}
	public void setCost(String cost) {
		this.cost = cost;
	}
	public String getStarttime() {
		return starttime;
	}
	public void setStarttime(String starttime) {
		this.starttime = starttime;
	}
	
	
}
